#include "../../minishell.h"

int pwd(char **envp)
{
	int x;

	x = -1;
	if (!envp)
		return (-1);
	while (envp[++x])
	{
		if (envp[x][0] == 'P' && envp[x][1] == 'W' && envp[x][2] == 'D' 
			&& envp[x][3] == '=')
		{
			printf("%s\n", envp[x] + 4);
			return (0);
		}
	}
	return (-1);
}